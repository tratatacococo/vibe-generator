import random

def generate_ai_idea(vibe: str) -> str:
    product = ["Приложение", "Сервис", "Игра", "Бот", "Платформа", "Сайт"]

    audience = [
        "для интровертов",
        "для тех, кто устал",
        "для ночных сов",
        "для людей, которые всё откладывают"
    ]

    if vibe == "weird":
        action = [
            "которое шепчет тебе правду по ночам",
            "которое врёт и знает об этом",
            "которое принимает решения за тебя",
            "которое слишком честное для этого мира"
        ]
        mood = ["абсурдно", "на грани", "как сон при температуре"]

    elif vibe == "serious":
        action = [
            "которое повышает продуктивность",
            "которое помогает фокусироваться",
            "которое упрощает сложные решения"
        ]
        mood = ["чётко", "прагматично", "без лишнего"]

    else:
        action = [
            "которое извиняется за тебя",
            "которое знает, что ты прокрастинируешь",
            "которое делает жизнь страннее"
        ]
        mood = ["с иронией", "по-доброму", "с лёгким вайбом"]

    return (
        f"{random.choice(product)} "
        f"{random.choice(action)} "
        f"{random.choice(audience)} "
        f"{random.choice(mood)}."
    )
